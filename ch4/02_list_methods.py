l1 = [0,1,34,2,45,0.1,5.22]
 
l1.sort()           # sorts the list in ascending order
print(l1)           

l1.append("hello")  # adds hello at the last of the list
print(l1)

l1.reverse()         # reverse the list
print(l1)

print(l1.pop(5))    # delets the value stored at index 5 and prints it
print(l1)